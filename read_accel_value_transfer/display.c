void Init_OLED(I2C_Type *base){
	OLED_Send_Command(base, 0xae);	//DISP OFF
	OLED_Send_Command(base, 0xd5);	//SETDISPCLKDIV
	OLED_Send_Command(base, 0x80);	//recommended ratio
	OLED_Send_Command(base, 0xa8);	//SETMULTIPLEX
	OLED_Send_Command(base, 63);	//HEIGHT-1
	OLED_Send_Command(base, 0xd3);	//SETDISPOFFSET
	OLED_Send_Command(base, 0x0);	//
	OLED_Send_Command(base, 0x40|0x0);	//SETSTARTLINE|0x0
	OLED_Send_Command(base, 0x8d);	//SETCHARGEPUMP
	OLED_Send_Command(base, 0x14);	//if vccstate!=SSD_1306_EXTERNALVCC
	OLED_Send_Command(base, 0x20);	//memory mode
	OLED_Send_Command(base, 0x0);	//
	OLED_Send_Command(base, 0xa0|0x1);	//SEGREMAP|0x1
	OLED_Send_Command(base, 0xc8);	//COMSCANDEC
	OLED_Send_Command(base, 0xda);
	OLED_Send_Command(base, 0x12);	//
	OLED_Send_Command(base, 0x81);	//recommended contrast
	OLED_Send_Command(base, 0xcf);	//if vccstate!=SSD_1306_EXTERNALVCC
	OLED_Send_Command(base, 0xd9);	//SETPRECHARGE
	OLED_Send_Command(base, 0xf1);	//if vccstate!=SSD_1306_EXTERNALVCC
	OLED_Send_Command(base, 0xdb);	//SETVCOMDETECT
	OLED_Send_Command(base, 0x40);	//
	OLED_Send_Command(base, 0xa4);	//DISPALLONRESUME
	OLED_Send_Command(base, 0xa6);	//NORMALDISP
	OLED_Send_Command(base, 0x2e);	//DISABLESCROLL
	OLED_Send_Command(base, 0xa5);	//DISPON
}


void OLED_Send_Command(I2C_Type *base, uint8_t command){
	I2C_Start(base, ADDR);
	I2C_Send_Byte(base, 0x00);
	I2C_Send_Byte(base, command);
	I2C_Stop(base);
}


void OLED_Display(I2C_Type *base){
	int i = 0;
	int x = 0;
	OLED_Send_Command(base, 0x21);
	OLED_Send_Command(base, 0x0);
	OLED_Send_Command(base, 127);
	OLED_Send_Command(base, 0x22);
	OLED_Send_Command(base, 0);
	OLED_Send_Command(base, 7);
	for (i=0;i<(128*8);i++){
		I2C_Start(base, ADDR);
		I2C_Send_Byte(base, 0x40);
		for(x=0;x<16;x++){
			I2C_Send_Byte(base, buffer[i]);
			i++;
		}
		i--;
		I2C_Stop(base);
	}
}


